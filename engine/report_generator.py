def generate_report(
    session_id:  str,
    filename:    str,
    sha256:      str,
    size_bytes:  int,
    stats,
    regions:     list,
    blocks:      list,
    custody=None,
    ml_summary:  Optional[dict] = None,
) -> dict:
    """
    Generate a complete forensic narrative report.

    Parameters
    ----------
    stats   : ScanStats object from scorer.py
    regions : list of Region objects from aggregator.py
    blocks  : list of BlockResult objects from classifier.py
    custody : CustodyChain object (optional)
    ml_summary : dict with ML classification summary stats

    Returns
    -------
    dict with all report sections, suitable for embedding in analysis JSON
    """
    now = datetime.now(timezone.utc).isoformat()

    # ── Compute derived statistics ────────────────────────────────────────────
    stats_d = stats.to_dict() if hasattr(stats, 'to_dict') else stats

    total     = stats_d["total_blocks"]
    susp      = stats_d["suspicious_blocks"]
    density   = stats_d["wipe_density"]
    score     = stats_d["intent_score"]
    verdict   = stats_d["verdict"]
    n_regions = stats_d["regions_count"]
    avg_ent   = stats_d["avg_entropy_flagged"]
    wipe_types = stats_d.get("wipe_type_counts", {})

    size_human = _fmt_bytes(size_bytes)
    total_blocks = total
    coverage_pct = round(density * 100, 2)

    # Dominant wipe type
    dominant_type = max(wipe_types, key=lambda k: wipe_types[k]) \
                    if wipe_types else "UNKNOWN"
    has_random    = wipe_types.get("RANDOM_WIPE", 0) > 0
    has_multi     = wipe_types.get("MULTI_PASS", 0) > 0
    has_zero      = wipe_types.get("ZERO_WIPE", 0) > 0
    has_ff        = wipe_types.get("FF_WIPE", 0) > 0

    # Region analysis
    region_dicts = [r.to_dict() if hasattr(r, 'to_dict') else r for r in regions]
    largest_region = max(region_dicts, key=lambda r: r.get("size", 0)) \
                     if region_dicts else None
    avg_region_size = (sum(r.get("size", 0) for r in region_dicts) / n_regions) \
                      if n_regions > 0 else 0

    # ── Section 1: Executive Summary ─────────────────────────────────────────
    executive_summary = _executive_summary(
        verdict, score, filename, size_human,
        coverage_pct, n_regions, has_random, has_multi,
        dominant_type, total_blocks, susp,
    )

    # ── Section 2: Technical Findings ────────────────────────────────────────
    technical_findings = _technical_findings(
        filename, sha256, size_bytes, size_human,
        total_blocks, susp, coverage_pct, n_regions,
        avg_ent, region_dicts, largest_region, avg_region_size,
    )

    # ── Section 3: Wipe Pattern Analysis ─────────────────────────────────────
    pattern_analysis = _pattern_analysis(
        wipe_types, region_dicts, has_random, has_multi,
        has_zero, has_ff,
    )

    # ── Section 4: Evidence Reliability ──────────────────────────────────────
    reliability = _reliability_assessment(
        score, verdict, density, n_regions,
        avg_ent, wipe_types, ml_summary,
    )

    # ── Section 5: ML Summary ────────────────────────────────────────────────
    ml_section = _ml_section(ml_summary)

    # ── Section 6: Conclusions ────────────────────────────────────────────────
    conclusions = _conclusions(
        verdict, score, filename, has_random, has_multi,
        n_regions, coverage_pct, density,
    )

    # ── Section 7: Recommended Actions ───────────────────────────────────────
    recommended_actions = _recommended_actions(
        verdict, has_random, has_multi, n_regions,
        score=score, has_zero=has_zero, has_ff=has_ff,
        coverage_pct=coverage_pct,
    )

    # ── Assemble final report ─────────────────────────────────────────────────
    report = {
        "generated_at": now,
        "session_id":   session_id,
        "report_version": "1.0",
        "examiner_note": (
            "This report was generated automatically by WipeTrace using "
            "rule-based and machine learning analysis of the provided disk image. "
            "It should be reviewed by a qualified digital forensics examiner before "
            "submission to any legal proceedings."
        ),
        "executive_summary": executive_summary,
        "technical_findings": technical_findings,
        "pattern_analysis":   pattern_analysis,
        "reliability":        reliability,
        "ml_analysis":        ml_section,
        "conclusions":        conclusions,
        "recommended_actions": recommended_actions,
        "metrics": {
            "intent_score":    score,
            "verdict":         verdict,
            "coverage_pct":    coverage_pct,
            "regions_found":   n_regions,
            "suspicious_pct":  round(susp / total * 100, 2) if total > 0 else 0,
            "dominant_type":   dominant_type,
            "has_multi_pass":  has_multi,
            "has_random_wipe": has_random,
        },
    }

    return report


